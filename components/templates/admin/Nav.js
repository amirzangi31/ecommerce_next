import { signOut } from "next-auth/react";
import Link from "next/link";
import { useRouter } from "next/router";
import React, { useEffect, useState } from "react";

function Nav() {
  const ls = typeof window !== "undefined" ? window.localStorage : null;

  const [darkMode, setDarkMode] = useState("")


  const setDarkModeFunc = () => {
    setDarkMode(ls?.theme)
    document.documentElement.className = ls?.theme
  }



  useEffect(() => {
    setDarkModeFunc()
  }, [])



  const darkModeHandler = () => {
    if (ls?.theme === "dark") {
      ls?.setItem("theme", "light")
      setDarkMode("light")
      document.documentElement.classList.add('light')
      document.documentElement.classList.remove('dark')
    } else {
      ls?.setItem("theme", "dark")
      setDarkMode("dark")
      document.documentElement.classList.add('dark')
      document.documentElement.classList.remove('light')
    }
  }


  const inactiveLink = `w-full flex  py-1 border-secondary-admin dark:border-dark-secondary-admin items-center text-whiteone dark:text-dark-whiteone bg-bg-admin dark:bg-dark-bg-admin rounded-lg my-2 px-2`;
  const activeLink = ` flex  py-1 border-secondary-admin dark:border-dark-secondary-admin items-center text-whiteone dark:text-dark-whiteone dark:bg-dark-secondary-admin  bg-secondary-admin rounded-lg my-2 px-2`;

  const router = useRouter()
  const { pathname } = router
  const logout = async () => {
    await signOut({
      redirect: false
    })
    router.replace("/signinadmin")
  }


  return (
    <aside>
      <div className="logo text-whiteone dark:text-dark-whiteone ">
        <Link href={"/admin/"}>پنل ادمین فروشگاه</Link>
      </div>

      <nav>
        <ul>
          <li>
            <button onClick={darkModeHandler} className={`w-full flex justify-center items-center ${darkMode === "light" ? activeLink : inactiveLink}`}>
              {
                ls?.theme === "dark" && darkMode === "dark" ? <div>
                  <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6">
                    <path strokeLinecap="round" strokeLinejoin="round" d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z" />
                  </svg>
                </div> : <div>
                  <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6">
                    <path strokeLinecap="round" strokeLinejoin="round" d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z" />
                  </svg>
                </div>
              }




            </button>
          </li>
          <li>
            <Link href={"/admin"} className={pathname === "/admin" ? activeLink : inactiveLink}>
              <svg
                xmlns="http://www.w3.org/2000/svg"
                fill="none"
                viewBox="0 0 24 24"
                strokeWidth={1.5}
                stroke="currentColor"
                className="w-6 h-6 ml-4"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M2.25 12l8.954-8.955c.44-.439 1.152-.439 1.591 0L21.75 12M4.5 9.75v10.125c0 .621.504 1.125 1.125 1.125H9.75v-4.875c0-.621.504-1.125 1.125-1.125h2.25c.621 0 1.125.504 1.125 1.125V21h4.125c.621 0 1.125-.504 1.125-1.125V9.75M8.25 21h8.25"
                />
              </svg>
              <div>خانه</div>
            </Link>
          </li>
          <li>
            <Link href={"/admin/products"} className={pathname.includes("/admin/products") ? activeLink : inactiveLink}>
              <svg
                xmlns="http://www.w3.org/2000/svg"
                fill="none"
                viewBox="0 0 24 24"
                strokeWidth={1.5}
                stroke="currentColor"
                className="w-6 h-6 ml-4"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M20.25 7.5l-.625 10.632a2.25 2.25 0 01-2.247 2.118H6.622a2.25 2.25 0 01-2.247-2.118L3.75 7.5M10 11.25h4M3.375 7.5h17.25c.621 0 1.125-.504 1.125-1.125v-1.5c0-.621-.504-1.125-1.125-1.125H3.375c-.621 0-1.125.504-1.125 1.125v1.5c0 .621.504 1.125 1.125 1.125z"
                />
              </svg>
              <div>محصولات</div>
            </Link>
          </li>
          <li>
            <Link href={"/admin/orders"} className={pathname.includes("/admin/orders") ? activeLink : inactiveLink}>
              <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor" className="w-6 h-6 ml-4">
                <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 10.5V6a3.75 3.75 0 10-7.5 0v4.5m11.356-1.993l1.263 12c.07.665-.45 1.243-1.119 1.243H4.25a1.125 1.125 0 01-1.12-1.243l1.264-12A1.125 1.125 0 015.513 7.5h12.974c.576 0 1.059.435 1.119 1.007zM8.625 10.5a.375.375 0 11-.75 0 .375.375 0 01.75 0zm7.5 0a.375.375 0 11-.75 0 .375.375 0 01.75 0z" />
              </svg>

              <div>سفارشات</div>
            </Link>
          </li>
          <li>
            <Link href={"/admin/articles"} className={pathname.includes("/admin/articles") ? activeLink : inactiveLink}>
              <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth="1.5" stroke="currentColor" className="w-6 h-6 ml-4">
                <path strokeLinecap="round" strokeLinejoin="round" d="M9 12h3.75M9 15h3.75M9 18h3.75m3 .75H18a2.25 2.25 0 002.25-2.25V6.108c0-1.135-.845-2.098-1.976-2.192a48.424 48.424 0 00-1.123-.08m-5.801 0c-.065.21-.1.433-.1.664 0 .414.336.75.75.75h4.5a.75.75 0 00.75-.75 2.25 2.25 0 00-.1-.664m-5.8 0A2.251 2.251 0 0113.5 2.25H15c1.012 0 1.867.668 2.15 1.586m-5.8 0c-.376.023-.75.05-1.124.08C9.095 4.01 8.25 4.973 8.25 6.108V8.25m0 0H4.875c-.621 0-1.125.504-1.125 1.125v11.25c0 .621.504 1.125 1.125 1.125h9.75c.621 0 1.125-.504 1.125-1.125V9.375c0-.621-.504-1.125-1.125-1.125H8.25zM6.75 12h.008v.008H6.75V12zm0 3h.008v.008H6.75V15zm0 3h.008v.008H6.75V18z" />
              </svg>


              <div>مقالات</div>
            </Link>
          </li>
          <li>
            <Link href={"/admin/categories"} className={pathname.includes("/admin/categories") ? activeLink : inactiveLink}>
              <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6 ml-4">
                <path strokeLinecap="round" strokeLinejoin="round" d="M8.25 6.75h12M8.25 12h12m-12 5.25h12M3.75 6.75h.007v.008H3.75V6.75zm.375 0a.375.375 0 11-.75 0 .375.375 0 01.75 0zM3.75 12h.007v.008H3.75V12zm.375 0a.375.375 0 11-.75 0 .375.375 0 01.75 0zm-.375 5.25h.007v.008H3.75v-.008zm.375 0a.375.375 0 11-.75 0 .375.375 0 01.75 0z" />
              </svg>

              <div>دسته بندی ها</div>
            </Link>
          </li>
          <li>
            <button onClick={logout} className={inactiveLink}>
              <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6 ml-4">
                <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" />
              </svg>

              <div>خروج</div>
            </button>
          </li>
        </ul>
      </nav>
    </aside>
  );
}

export default Nav;
